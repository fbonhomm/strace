#include "libft.h"

int		ft_strch_EOF(char *str)
{
	int i;

	i = 0;
	while (str[i] && str[i] != EOF)
		i++;
	if ((str[i] == -1) || (str[i + 1] == -1))
		return (-1);
	return (0);
}