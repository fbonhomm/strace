#include "libft.h"

char		*ft_strstr(const char *s1, const char *s2)
{
	int		i[3];

	if (ft_strlen(s2) == 0)
		return ((char *)s1);
	i[0] = 0;
	while (s1 && s1[i[0]])
	{
		if (s1[i[0]] == s2[0])
		{
			i[2] = i[0];
			i[1] = 0;
			while (s1[i[2]] == s2[i[1]] && s2[i[1]])
			{
				i[2]++;
				i[1]++;
			}
			if (s2[i[1]] == '\0')
				return ((char *)&s1[i[0]]);
		}
		i[0]++;
	}
	return (NULL);
}
